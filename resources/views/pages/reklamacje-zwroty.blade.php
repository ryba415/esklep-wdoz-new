<h2 class="text-big-bold text-2xl mt-4 mb-4">Reklamacje</h2>

<p class="mb-3">Kupujący ma prawo do zgłoszenia reklamacji zgodnie z Regulaminem wracamdozdrowia.pl.<br></p>

<p class="mb-3">Reklamacje prosimy składać drogą mailową na adres: <a class="text-[#008641] font-bold" href="mailto:reklamacje@wdoz.pl">reklamacje@wdoz.pl</a>. Reklamacja powinna zawierać dokładny opis reklamacji, numer zamówienia oraz dane klienta.</p>

<p class="mb-3">Reklamacje rozpatrywane są w ciągu 14 dni od dnia otrzymania reklamowanego towaru. Po rozpatrzeniu reklamacji skontaktuje się z Państwem pracownik Działu Obsług Klienta i przekaże decyzję reklamacyjną oraz wszelkie niezbędne informacje.</p>

<p class="mb-3">Pliki do pobrania:</p>

<p><a class="text-[#008641] font-bold" href="/downloads/formularz_reklamacyjny.docx">Formularz reklamacyjny (.docx)</a></p>



<h2 class="text-big-bold text-2xl mt-8 mb-4">Zwroty</h2>

<p class="mb-3">Klient ma prawo odstąpienia od niniejszej umowy w terminie 14 dni bez podania jakiejkolwiek przyczyny.</p>

<p class="mb-3">Termin do odstąpienia od umowy wygasa po upływie 14 dni od dnia, w którym weszli państwo w posiadanie rzeczy lub w którym osoba trzecia inna niż przewoźnik i wskazana przez Państwa weszła w ich posiadanie.</p>

<p class="mb-3">Zwracany towar musi być w stanie nienaruszonym. Klient ponosi bezpośrednie koszty zwracanego towaru.</p>

<p class="mb-3"><span style="color:#FF0000;"><strong>Produkty lecznicze, środki spożywcze specjalnego przeznaczenia żywieniowego i wyroby medyczne wydane z wracamdozdrowia.pl nie podlegają zwrotowi, &nbsp;za wyjątkiem&nbsp; produktu leczniczego lub wyrobu medycznego zwracanego z powodu wady jakościowej, niewłaściwego ich wydania lub sfałszowania produktu leczniczego.</strong></span></p>

