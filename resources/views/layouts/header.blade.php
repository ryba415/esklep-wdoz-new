<script>
(() => {
  const SEARCH_URL = "https://test.wracamdozdrowia.pl/search";
  const MIN_CHARS = 2;
  const DEBOUNCE_MS = 100;
  const MAX_ITEMS = 6;

  function debounce(fn, wait) {
    let t;
    return (...args) => {
      clearTimeout(t);
      t = setTimeout(() => fn(...args), wait);
    };
  }

  function escapeHtml(str) {
    return String(str ?? "")
      .replaceAll("&", "&amp;")
      .replaceAll("<", "&lt;")
      .replaceAll(">", "&gt;")
      .replaceAll('"', "&quot;")
      .replaceAll("'", "&#039;");
  }

function normalizeProducts(json) {
  const products = Array.isArray(json?.products) ? json.products : [];

  const total =
    Number(json?.allProductsInCategoryCount) ||
    Number(json?.pagination?.total) ||
    Number(json?.total) ||
    Number(json?.meta?.total) ||
    0;

  return { products, total };
}


  function getProductUrl(p) {
    if (p?.url) return p.url;
    if (p?.slug) return "/" + p.slug;
    if (p?.id) return "/" + p.id;
    return "#";
  }

  function getImageUrl(p) {
    if (!p?.image_name) return "";
    return "/uploads/images/product/" + p.image_name;
  }

  function renderDropdown(dropdownEl, query, products, totalCount = 0) {
    const inner = dropdownEl.querySelector(".search-dropdown__inner");
    if (!inner) return;

    if (!query || query.length < MIN_CHARS) {
      dropdownEl.hidden = true;
      inner.innerHTML = "";
      return;
    }

    // Brak wyników
    if (!products.length) {
      inner.innerHTML = `
        <div class="search-dropdown__empty">
          Brak wyników wyszukiwania
        </div>
        <a class="search-dropdown__all" href="/search?query=${encodeURIComponent(query)}">
            Wszystkie produkty (${totalCount.toLocaleString('pl-PL')})
        </a>
      `;
      dropdownEl.hidden = false;
      return;
    }

    const items = products.slice(0, MAX_ITEMS).map(p => {
      const name = escapeHtml(p?.name);
      const category = escapeHtml(p?.category_name || "Apteczny"); // dopasuj jak masz kategorię
      const url = getProductUrl(p);
      const img = getImageUrl(p);

      return `
        <a class="search-dropdown__item" href="${escapeHtml(url)}">
          <div class="search-dropdown__img">
            ${img ? `<img src="${escapeHtml(img)}" alt="${name}" loading="lazy">` : `<div class="search-dropdown__img--ph"></div>`}
          </div>
          <div class="search-dropdown__txt">
            <div class="search-dropdown__meta">${category}</div>
            <div class="search-dropdown__name">${name}</div>
          </div>
        </a>
      `;
    }).join("");

    inner.innerHTML = `
      <div class="search-dropdown__list">
        ${items}
      </div>
        <a class="search-dropdown__all" href="/search?query=${encodeURIComponent(query)}">
            Wszystkie produkty (${totalCount.toLocaleString('pl-PL')})
        </a>
    `;
    dropdownEl.hidden = false;
  }

function attachAutocomplete(inputEl, dropdownEl) {
  if (!inputEl || !dropdownEl) return;

  let abortController = null;

  const doSearch = debounce(async () => {
    const query = inputEl.value.trim();

    // Zawsze chowaj dropdown, dopóki nie ma min 2 znaków
    if (query.length < MIN_CHARS) {
      dropdownEl.hidden = true;
      const inner = dropdownEl.querySelector(".search-dropdown__inner");
      if (inner) inner.innerHTML = "";
      return;
    }

    // anuluj poprzedni request
    if (abortController) abortController.abort();
    abortController = new AbortController();

    // loader
    const inner = dropdownEl.querySelector(".search-dropdown__inner");
    if (inner) inner.innerHTML = `<div class="search-dropdown__loading">Szukam…</div>`;
    dropdownEl.hidden = false;

    try {
      const url = `${SEARCH_URL}?query=${encodeURIComponent(query)}&sort-by=best-sort`;
      const res = await fetch(url, {
        method: "GET",
        headers: { "Accept": "application/json" },
        signal: abortController.signal
      });

      if (!res.ok) throw new Error("HTTP " + res.status);

      const json = await res.json();
      const data = normalizeProducts(json);

      // jeśli user zdążył zmienić wpis
      if (inputEl.value.trim() !== query) return;

      renderDropdown(dropdownEl, query, data.products, data.total);
    } catch (err) {
      if (err.name === "AbortError") return;

      const inner2 = dropdownEl.querySelector(".search-dropdown__inner");
      dropdownEl.hidden = false;
      if (inner2) {
        inner2.innerHTML = `
          <div class="search-dropdown__empty">
            Wystąpił błąd. Spróbuj ponownie.
          </div>
          <a class="search-dropdown__all" href="/search?query=${encodeURIComponent(query)}">
            Wszystkie produkty
          </a>
        `;
      }
    }
  }, DEBOUNCE_MS);

  inputEl.addEventListener("input", doSearch);

  document.addEventListener("click", (e) => {
    const wrapper = dropdownEl.closest(".header-search-autocomplete");
    if (!wrapper) return;
    if (!wrapper.contains(e.target)) dropdownEl.hidden = true;
  });

  inputEl.addEventListener("keydown", (e) => {
    if (e.key === "Escape") dropdownEl.hidden = true;
    if (e.key === "Enter") dropdownEl.hidden = true;
  });

  // Focus: pokaż tylko jeśli query >= 2 i dropdown ma realną treść
  inputEl.addEventListener("focus", () => {
    const q = inputEl.value.trim();
    if (q.length < MIN_CHARS) {
      dropdownEl.hidden = true;
      return;
    }
    const hasItems = dropdownEl.querySelector(".search-dropdown__item, .search-dropdown__empty, .search-dropdown__loading");
    dropdownEl.hidden = !hasItems;
  });
}


  // Desktop
  attachAutocomplete(
    document.getElementById("header-search-input"),
    document.getElementById("header-search-dropdown")
  );

  // Mobile
  attachAutocomplete(
    document.getElementById("header-search-input-mobile"),
    document.getElementById("header-search-dropdown-mobile")
  );
})();
</script>


