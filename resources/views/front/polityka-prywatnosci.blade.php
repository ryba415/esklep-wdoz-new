<h2>7. PRAWA OSÓB, KTÓREJ DANE DOTYCZĄ</h2>
<ul>
<li><span class="list-type">7.1.</span> Osoba, której dane dotyczą, ma prawo żądania od Administratora dostępu do swoich danych
osobowych, ich sprostowania, usunięcia („prawo do bycia zapomnianym”) lub ograniczenia
przetwarzania oraz ma prawo do wniesienia sprzeciwu wobec przetwarzania, a także ma
prawo do przenoszenia swoich danych. Szczegółowe warunki wykonywania wskazanych
wyżej praw wskazane są w art. 15-21 Rozporządzenia.</li>
<li><span class="list-type">7.2.</span> Osoba, której dane przetwarzane są przez Administratora na podstawie wyrażonej zgody (na
podstawie art. 6 ust. 1 lit. a) lub art. 9 ust. 2 lit. a) Rozporządzenia), ma prawo do cofnięcia
zgody w dowolnym momencie bez wpływu na zgodność z prawem przetwarzania, którego
dokonano na podstawie zgody przed jej cofnięciem.</li>
<li><span class="list-type">7.3.</span> Osoba, której dane przetwarzane są przez Administratora, ma prawo wniesienia skargi do
organu nadzorczego w sposób i trybie określonym w przepisach Rozporządzenia oraz prawa
polskiego, w szczególności ustawy o ochronie danych osobowych. Organem nadzorczym w
Polsce jest Prezes Urzędu Ochrony Danych Osobowych.</li>
<li><span class="list-type">7.4.</span> Osoba, której dane dotyczą, ma prawo w dowolnym momencie wnieść sprzeciw – z przyczyn
związanych z jej szczególną sytuacją – wobec przetwarzania dotyczących jej danych
osobowych opartego na art. 6 ust. 1 lit. e) (interes lub zadania publiczne) lub f) (prawnie
uzasadniony interes administratora), w tym profilowania na podstawie tych przepisów.
Administratorowi w takim przypadku nie wolno już przetwarzać tych danych osobowych,
chyba że wykaże on istnienie ważnych prawnie uzasadnionych podstaw do przetwarzania,
nadrzędnych wobec interesów, praw i wolności osoby, której dane dotyczą, lub podstaw do
ustalenia, dochodzenia lub obrony roszczeń.</li>
</ul>

<h2>8. PLIKI COOKIES I PROFILOWANIE</h2>
<ul>
<li><span class="list-type">8.1.</span> Pliki cookie (ciasteczka) to małe pliki tekstowe, które mogą być stosowane przez strony
internetowe, aby użytkownicy mogli korzystać ze stron w bardziej sprawny sposób.</li>
<li><span class="list-type">8.2.</span> Administrator wykorzystuje pliki cookie niezbędne do funkcjonowania Serwisu, w tym
niezbędne do świadczenia Usług Elektronicznych.</li>
<li><span class="list-type">8.3.</span> Pliki cookie są wykorzystywane przez Administratora do prawidłowego i bezpiecznego
funkcjonowania Serwisu, w związku z czym nie jest wymagana zgoda Użytkownika na
korzystanie z nich.</li>
<li><span class="list-type">8.4.</span> Pliki cookie nie są wykorzystywane do celów marketingowych lub reklamowych. W
przypadku podjęcia przez Usługodawcę zmiany decyzji w tym zakresie, zmianie ulegną
odpowiednie postanowienia polityki prywatności.</li>
<li><span class="list-type">8.5.</span> Administrator nie profiluje dany osobowych Użytkowników tj. nie poddaje danych
osobowych zautomatyzowanemu przetwarzaniu.</li>
</ul>

