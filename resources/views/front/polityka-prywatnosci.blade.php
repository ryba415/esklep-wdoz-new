<h2>6. ODBIORCY DANYCH</h2>
<ul>
<li><span class="list-type">6.1.</span> Dla prawidłowego funkcjonowania Serwisu Internetowego, w tym dla realizacji zawieranych
umów konieczne jest korzystanie przez Administratora z usług podmiotów zewnętrznych
(takich jak np. dostawca oprogramowania).</li>
<li><span class="list-type">6.2.</span> Administrator korzysta z następujących podmiotów zewnętrznych przetwarzających dane
