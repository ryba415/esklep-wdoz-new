(c) dostawcy systemu do fakturowania, w którym to systemie przetwarzane są dane
niezbędne do wystawianie faktur na rzecz Użytkowników;
(d) dostawcy usług hostingowych, który przechowuje dane osobowe Użytkowników na
serwerze.</li>
<li><span class="list-type">6.3.</span> Dane osobowe Użytkowników mogą być również przekazywane do urzędów skarbowych w
